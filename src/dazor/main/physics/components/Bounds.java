package dazor.main.physics.components;

public class Bounds {

	float radius;

	public Bounds() {
		
	}

	public Bounds(float radius) {
		super();
		this.radius = radius;
	}

	public float getRadius() {
		return radius;
	}

	public void setRadius(float radius) {
		this.radius = radius;
	}

}
